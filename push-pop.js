var numbers = [12, 14, 16, 20];
console.log(numbers);
// use push to add element to an array as the last element 
numbers.push(89);
console.log(numbers);

var friendsName = ['Sadia', 'Sudha', 'Sristy', 'Samiya'];
console.log(friendsName);
friendsName.push('Rabeya');
console.log(friendsName);
friendsName.push('Mridha');
console.log(friendsName);

// use pop to get(remove) last eliment
numbers.pop();
console.log(numbers);

// to add eliment at the beginning of an array
friendsName.unshift('Taposhi');
console.log(friendsName);